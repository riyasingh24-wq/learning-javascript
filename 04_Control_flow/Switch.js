const month = "april"
switch (month) {
      case "jan":
            console.log("January");
            break;
      case "feb":
            console.log("february");
            break;
      case "march":
            console.log("march");
            break;
      case "april":
            console.log("april");
            break;

      default:
            console.log("default case match");
            break;
}